def build_suggested_strategies(target_scored: dict) -> list[dict]:
    hint = (target_scored.get("regime_hint") or "").lower()
    priors = REGIME_KNOB_PRIORS.get(hint) or REGIME_KNOB_PRIORS.get("scheduler_tail")
    out = []
    for i, p in enumerate(priors, start=1):
        out.append({
            "strategy_id": f"S{i:03d}",
            "kind": "config",
            "route_to": "config-agent",
            "knob": p["knob"],
            "direction": p.get("direction"),
            "values_to_try": p["values_to_try"],
            "expected_improvement_pct": p["expected_improvement_pct"],
            "rationale": p["rationale"],
            "risk": "low for v0.4; verify via controls"
        })
    return out


def build_evidence(target_scored: dict) -> dict:
    """Translate scored row → problem.evidence block."""
    components = target_scored.get("components") or {}
    signals: list[dict] = []

    def push(source: str, field: str, value: Any, weight: str) -> None:
        if value is None:
            return
        signals.append({
            "source": source, "field": field, "value": value, "weight": weight
        })

    sls = components.get("server_log_signal", {}).get("evidence", {})
    push("server_features.json", "concurrency_capped",
         sls.get("contributions", {}).get("concurrency_capped") is not None,
         "high")
    push("server_features.json", "peak_queue_reqs", sls.get("peak_queue_reqs"), "high")
    push("server_features.json", "max_running_requests", sls.get("max_running_requests"), "high")
    push("server_features.json", "peak_token_usage", sls.get("peak_token_usage"), "medium")

    cls = components.get("failure_class", {}).get("evidence", {})
    push("classification.json", "classification", cls.get("classification"), "high")

    tlr = components.get("tail_latency_ratio", {}).get("evidence", {})
    push("baseline_metrics.json", "tail_p99_over_p50", tlr.get("max_ratio"), "medium")

    ln = components.get("local_nonlinearity_primary", {}).get("evidence", {})
    push("baseline_metrics.json", "ratio_worse_than_nearest_neighbor",
         ln.get("ratio_worse_than_nb"), "high")

    layers = ["L1", "L2", "L3"]
    if any(s["source"] == "profile_summary.json" for s in signals):
        layers.append("L4")

    return {
        "layers_collected": layers,
        "key_signals": signals,
        "bias_check": {
            "repeats_n": 1,
            "primary_metric_cv_pct": None,
            "metric_stable": None,
            "note": "v0.4: single-repeat; setter agent did not opt in to L5 bias removal",
        },
        "ruled_out": [],
    }


def build_hypothesis_md(target_scored: dict, target_workload: dict, neighbors: list[dict]) -> str:
    name = target_scored["workload_name"]
    score = target_scored.get("score")
    classification = target_scored.get("classification")
    primary_metric = target_scored.get("primary_metric")
    primary_value = target_scored.get("primary_value")

    lines = []
    lines.append(f"# Hypothesis for {name}\n")
    lines.append("## Symptom\n")
    lines.append(
        f"The target workload `{name}` produced "
        f"`{primary_metric} = {primary_value:.2f}` (lower-is-better). "
        f"Classification: **{classification}**. Suspicion score: **{score:.3f}**.\n"
    )

    if neighbors:
        lines.append("## Why we believe this is a cliff, not noise\n")
        for n in neighbors:
            m = n.get("metrics") or {}
            v = m.get(primary_metric)
            wl = load_yaml(Path(n["workload_file"]))
            axis_val = (wl.get("source") or {}).get("axis_value")
            axis = (wl.get("source") or {}).get("axis")
            if v is not None:
                lines.append(
                    f"- Neighbor `{n['workload_name']}` ({axis}={axis_val}) "
                    f"has `{primary_metric} = {v:.2f}` — bracket evidence.\n"
                )

    components = target_scored.get("components") or {}
    sls = components.get("server_log_signal", {}).get("evidence", {})
    if sls.get("contributions"):
        lines.append("\n## Server-log evidence\n")
        for k, _ in sls["contributions"].items():
            lines.append(f"- `{k}` is True\n")
        for fld in ("peak_running_reqs", "peak_queue_reqs",
                    "max_running_requests", "peak_token_usage"):
            v = sls.get(fld)
            if v is not None:
                lines.append(f"- `{fld}` = {v}\n")

    lines.append("\n## Suggested mechanism\n")
    hint = (target_scored.get("regime_hint") or "").lower()
    if "scheduler" in hint and sls.get("contributions", {}).get("concurrency_capped"):
        lines.append(
            "The server's admission cap (`max_running_requests`) is below the "
            "workload's `max_concurrency`; requests queue and observe long TTFT. "
            "Raising the cap should reduce queueing without hurting steady-state.\n"
        )
    elif "prefill" in hint:
        lines.append(
            "Long prefill cost is the dominant TTFT term; chunked prefill or a "
            "different prefill batch budget may help.\n"
        )
    elif "decode" in hint:
        lines.append(
            "Decode throughput is the primary constraint; reducing per-token "
            "scheduler overhead (e.g. `num-continuous-decode-steps`) should help.\n"
        )
    elif "prefix" in hint or "cache" in hint:
        lines.append(
            "Prefix-cache effectiveness depends on schedule policy and "
            "eviction settings; LPM scheduling and radix-cache ablation are the "
            "primary knobs.\n"
        )
    else:
        lines.append(
            "Mechanism is uncertain. Solver should run a deep profile (L4) "
            "before committing to a strategy.\n"
        )

    lines.append(
        "\n## Status\n\n"
        "This hypothesis was **auto-drafted** by `select_problems.py` from "
        "rule-based scoring evidence. A setter or solver agent should refine "
        "it once they have profile data.\n"
    )
    return "".join(lines)


def build_acceptance_criteria(target_scored: dict) -> dict:
    primary = target_scored.get("primary_metric") or "ttft_p95_ms"
    direction = target_scored.get("primary_direction") or "lower"
    return {
        "schema_version": 1,
        "primary": {
            "on": "target",
            "metric": primary,
            "direction": direction,
            "required_improvement_pct": 30.0,
            "stretch_improvement_pct": 60.0,
        },
        "constraints": [
            {"on": "target", "metric": "request_throughput",
             "direction": "higher", "max_regression_pct": 10.0},
            {"on": "controls.smoke", "metric": "ttft_p95_ms",
             "direction": "lower", "max_regression_pct": 5.0},
            {"on": "any", "metric": "oom", "max": 0},
            {"on": "any", "metric": "server_crash", "max": 0},
            {"on": "any", "metric": "failed_requests", "max": 0},
        ],
        "verification": {
            "always_paired_ab": False,
            "paired_ab_required_when_delta_lt_pct": 5.0,
            "min_repeats_per_workload_for_keep": 3,
        },
        "stretch_goals": [
            "Neighbor workloads also improve or remain unchanged.",
        ],
    }


# ---------- main ----------

def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument("--raw", default="regime_scout/outputs/raw_results.jsonl")
    ap.add_argument("--suspicious", default="regime_scout/outputs/suspicious_cases.jsonl")
    ap.add_argument("--server-config", default="configs/base.yaml")
    ap.add_argument("--problems-root", default="experiments/problems")
    ap.add_argument("--out", default="regime_scout/outputs/selected_problems.jsonl")
    ap.add_argument("--max-problems", type=int, default=5)
    ap.add_argument("--threshold", type=float, default=0.30)
    args = ap.parse_args()

    scored_rows = read_jsonl(args.suspicious)
    raw_rows = read_jsonl(args.raw)
    if not scored_rows:
        print(f"[select_problems] no scored rows in {args.suspicious}", file=sys.stderr)
        return 1

    raw_by_run: dict[str, dict] = {r["run_id"]: r for r in raw_rows}

    chosen = pick_problems(scored_rows, raw_by_run, args.threshold, args.max_problems)
    if not chosen:
        print(f"[select_problems] no cases met threshold {args.threshold}", file=sys.stderr)
        # still emit empty file so downstream knows we ran
        Path(args.out).write_text("")
        return 0

    server_cfg = load_yaml(args.server_config) if Path(args.server_config).exists() else {}
    problems_root = Path(args.problems_root)
    problems_root.mkdir(parents=True, exist_ok=True)

    out_jsonl = Path(args.out)
    if out_jsonl.exists():
        out_jsonl.unlink()

    for idx, target_scored in enumerate(chosen, start=1):
        problem_id = f"P{idx:03d}"
        problem_dir = problems_root / problem_id
        problem_dir.mkdir(parents=True, exist_ok=True)
        (problem_dir / "attempts").mkdir(exist_ok=True)

        target_run = raw_by_run.get(target_scored["run_id"])
        if not target_run:
            print(f"[select_problems] {problem_id}: no raw row for "
                  f"{target_scored['run_id']}; skipping", file=sys.stderr)
            continue

        # ---- target ----
        target_block = assemble_target_block(target_run, problem_dir)

        # ---- neighbors ----
        neighbors_dir = problem_dir / "neighbors"
        neighbors_dir.mkdir(exist_ok=True)
        neighbors_raw = find_neighbors(target_scored, raw_by_run)
        if not neighbors_raw:
            print(f"[select_problems] {problem_id}: no neighbors found; "
                  f"problem package is incomplete (skip later).", file=sys.stderr)
        neighbor_blocks = []
        for n in neighbors_raw:
            blk = assemble_workload_block(n, neighbors_dir, "neighbors")
            wl = load_yaml(Path(n["workload_file"]))
            axis = (wl.get("source") or {}).get("axis")
            axis_val = (wl.get("source") or {}).get("axis_value")
            blk["role"] = f"{axis}_neighbor" if axis else "neighbor"
            blk["axis"] = axis
            blk["axis_value"] = axis_val
            blk["expected_after_fix"] = "small_improvement_or_no_change"
            neighbor_blocks.append(blk)

        # ---- controls ----
        controls_dir = problem_dir / "controls"
        controls_dir.mkdir(exist_ok=True)
        controls_raw = find_controls(target_scored, raw_by_run, n=1)
        control_blocks = []
        for c in controls_raw:
            blk = assemble_workload_block(c, controls_dir, "controls")
            blk["role"] = "no_regression_check"
            control_blocks.append(blk)

        # ---- evidence ----
        evidence = build_evidence(target_scored)

        # ---- hypothesis ----
        hypothesis_md = build_hypothesis_md(
            target_scored, load_yaml(Path(target_run["workload_file"])), neighbors_raw
        )
        (problem_dir / "hypothesis.md").write_text(hypothesis_md)

        # ---- acceptance ----
        accept = build_acceptance_criteria(target_scored)
        save_json(accept, problem_dir / "acceptance_criteria.json")

        # ---- suggested strategies ----
        strategies = build_suggested_strategies(target_scored)

        # ---- final problem.json ----
        symptom = {
            "metric": target_scored.get("primary_metric"),
            "direction": "lower-is-better" if target_scored.get("primary_direction") == "lower"
                         else "higher-is-better",
            "observed_value": target_scored.get("primary_value"),
            "description": f"score {target_scored.get('score')} on "
                           f"{target_scored.get('primary_metric')} "
                           f"({target_scored.get('classification')})",
        }
        # nearest-neighbor ratio if we can compute it
        if neighbors_raw:
            nb_metric = (neighbors_raw[0].get("metrics") or {}).get(
                target_scored.get("primary_metric"))
            if nb_metric and target_scored.get("primary_value"):
                symptom["magnitude_vs_nearest_neighbor_pct"] = round(
                    100 * (target_scored["primary_value"] / nb_metric - 1.0), 1
                )

        problem = {
            "schema_version": 1,
            "problem_id": problem_id,
            "regime_id": f"R_{target_scored.get('regime_hint')}",
            "created_at": now_str(),
            "frozen": True,
            "model_path": server_cfg.get("model-path"),
            "hardware": "H200",
            "sglang_version": None,
            "sglang_commit": None,
            "baseline_config": str(Path(args.server_config).resolve()),
            "target": target_block,
            "symptom": symptom,
            "benchmark_suite": {
                "target": {
                    "workload": target_block["workload"],
                    "role": "must_improve",
                    "primary_metric": target_scored.get("primary_metric"),
                },
                "neighbors": neighbor_blocks,
                "controls": control_blocks,
            },
            "evidence": evidence,
            "hypothesis_md": "hypothesis.md",
            "suggested_strategies": strategies,
            "rejected_strategies": [],
            "acceptance_criteria_file": "acceptance_criteria.json",
            "lineage": {
                "stage1_run_dir": target_run.get("run_dir"),
                "neighbor_run_dirs": [n.get("run_dir") for n in neighbors_raw],
                "control_run_dirs": [c.get("run_dir") for c in controls_raw],
                "scoring_record": (
                    f"{args.suspicious}#run_id={target_scored['run_id']}"
                ),
                "noise_baseline": None,
            },
            "note_v04": (
                "Produced by select_problems.py from v0.3 rule-based scoring + "
                "wave-1 boundary expansion. L4 profile and L5 repeats were "
                "skipped (agent-decided). attempts/ is empty and awaits solver."
            ),
        }
        save_json(problem, problem_dir / "problem.json")

        # one-line index entry
        append_jsonl({
            "problem_id": problem_id,
            "regime_id": problem["regime_id"],
            "problem_dir": str(problem_dir),
            "problem_json": str(problem_dir / "problem.json"),
            "workload_yaml": str(problem_dir / "workload.yaml"),
            "primary_metric": target_scored.get("primary_metric"),
            "primary_value": target_scored.get("primary_value"),
            "suspicion_score": target_scored.get("score"),
            "n_neighbors": len(neighbor_blocks),
            "n_controls": len(control_blocks),
        }, out_jsonl)

        print(f"[select_problems] {problem_id}: {target_scored['workload_name']} "
              f"(score={target_scored.get('score')}, {len(neighbor_blocks)} neighbors, "
              f"{len(control_blocks)} controls) → {problem_dir}")

    print(f"[select_problems] {len(chosen)} problem(s) → {args.out}")
    return 0


