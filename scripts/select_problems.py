#!/usr/bin/env python3
"""Stage 1: assemble v1 problem packages from suspicious cases.

This is the v0.4 replacement for `select_cases_for_stage2.py`. It produces
**self-contained problem packages** under `experiments/problems/PNNN/`
matching the schema in `docs/problem-package/schema.md`.

Each problem package contains:
  - problem.json              (umbrella contract)
  - workload.yaml             (target, frozen copy)
  - baseline_metrics.json     (L1: target metrics)
  - server_features.json      (L2: target log mining)
  - classification.json       (L3: target classification)
  - profile_summary.json      (L4: null for v0.4 — agent-decided)
  - neighbors/                (boundary-expansion neighbors + their metrics + features)
  - controls/                 (unrelated workloads as negative control)
  - hypothesis.md             (auto-drafted from evidence; setter polishes)
  - acceptance_criteria.json  (auto-drafted from scoring)
  - attempts/                 (empty; solver writes here)

Rules:
  1. Score ≥ threshold OR hard failure → selected.
  2. At most 1 problem per regime_hint cluster (diversity).
  3. At most --max-problems total.
  4. Every problem MUST have ≥1 neighbor (from the boundary-expansion data already
     in raw_results.jsonl). Without a neighbor, skip and emit a warning.
  5. Every problem MUST have ≥1 control (preferred: a clean-pass workload with a
     different regime_hint).

After this script runs, anyone can:
  - tar/zip the problem dir and share it
  - run a solver against it
  - re-validate the cliff by re-running the target workload
"""
from __future__ import annotations

