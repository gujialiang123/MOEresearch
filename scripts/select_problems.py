# ---------- selection ----------

def pick_problems(
    scored: list[dict],
    raw_by_run: dict[str, dict],
    threshold: float,
    max_problems: int,
) -> list[dict]:
    """Choose which scored cases become problems."""
    chosen: list[dict] = []
    seen_clusters: set[str] = set()

    # Hard failures always selected first
    for s in scored:
        if s.get("status") == "fail" and (s.get("failure_kind") or "") in {
            "oom",
            "server_crash",
            "timeout",
        }:
            chosen.append(s)
            seen_clusters.add(s.get("regime_hint"))
            if len(chosen) >= max_problems:
                return chosen

    # Then top-scored passed runs, one per cluster
    for s in scored:
        if s.get("status") != "pass":
            continue
        if (s.get("score") or 0.0) < threshold:
            continue
        hint = s.get("regime_hint")
        if hint in seen_clusters:
            continue
        chosen.append(s)
        seen_clusters.add(hint)
        if len(chosen) >= max_problems:
            break
    return chosen


# ---------- neighbor + control discovery ----------

def find_neighbors(
    target_scored: dict, raw_by_run: dict[str, dict]
) -> list[dict]:
    """A neighbor is a passed run with the same regime_hint as the target,
    different workload, generated by boundary-expansion.

    We detect boundary-expansion outputs by the workload yaml having a
    `source.generated_by == "boundary-expansion"` field, AND the parent
    pointing at the target's workload file.
    """
    target_workload = target_scored["workload_file"]
    target_hint = target_scored.get("regime_hint")
    neighbors: list[dict] = []
    for run_id, rec in raw_by_run.items():
        if rec.get("status") != "pass":
            continue
        if rec.get("regime_hint") != target_hint:
            continue
        if rec["workload_file"] == target_workload:
            continue
        wl = Path(rec["workload_file"])
        if not wl.exists():
            continue
        wl_doc = load_yaml(wl)
        src = wl_doc.get("source") or {}
        if src.get("generated_by") != "boundary-expansion":
            continue
        if Path(src.get("parent", "")).resolve() != Path(target_workload).resolve():
            continue
        neighbors.append(rec)
    # sort by axis value for deterministic ordering
    neighbors.sort(key=lambda r: (
        (load_yaml(Path(r["workload_file"])).get("source") or {}).get("axis_value", 0)
    ))
    return neighbors


def find_controls(
    target_scored: dict, raw_by_run: dict[str, dict], n: int = 1
) -> list[dict]:
    """A control is any passed clean run with a DIFFERENT regime_hint and a
    fundamentally different dataset shape — so a fix on the target should not
    affect controls (much).

    Strategy: prefer `smoke` (the canonical noise-floor workload); fall back
    to other passed clean-pass workloads in different hints.
    """
    target_hint = target_scored.get("regime_hint")
    controls: list[dict] = []

    # Prefer smoke
    for rec in raw_by_run.values():
        if rec.get("status") != "pass":
            continue
        if rec.get("regime_hint") == target_hint:
            continue
        if (rec.get("workload_name") or "").startswith("smoke"):
            controls.append(rec)
            break

    # Fill with other distinct-hint clean passes
    seen_hints = {target_hint} | {r.get("regime_hint") for r in controls}
    for rec in raw_by_run.values():
        if len(controls) >= n:
            break
        if rec.get("status") != "pass":
            continue
        if rec.get("regime_hint") in seen_hints:
            continue
        controls.append(rec)
        seen_hints.add(rec.get("regime_hint"))

    return controls[:max(n, 1)]


# ---------- per-package assembly ----------

def copy_file(src: Path, dst: Path) -> None:
    dst.parent.mkdir(parents=True, exist_ok=True)
    if src.exists():
        dst.write_bytes(src.read_bytes())


def assemble_workload_block(
    rec: dict, dest_dir: Path, prefix: str
) -> dict:
    """Copy workload yaml + metrics + (best-effort) server features into dest_dir.
    Returns a dict describing where things landed (relative paths from
    dest_dir's parent = the problem dir)."""
    name = rec["workload_name"]
    run_dir = Path(rec["run_dir"]) if rec.get("run_dir") else None
    src_wl = Path(rec["workload_file"])

    # workload yaml
    wl_dest = dest_dir / f"{name}.yaml"
    copy_file(src_wl, wl_dest)

    # metrics (canonical name `*_metrics.json` in run_dir)
    metrics_dest = dest_dir / f"{name}_baseline_metrics.json"
    if run_dir and run_dir.exists():
        cand = list(run_dir.glob("*_metrics.json"))
        if cand:
            copy_file(cand[0], metrics_dest)

    # server features (optional)
    features_dest = dest_dir / f"{name}_server_features.json"
    if run_dir and (run_dir / "server_features.json").exists():
        copy_file(run_dir / "server_features.json", features_dest)

    classification_dest = dest_dir / f"{name}_classification.json"
    if run_dir and (run_dir / "classification.json").exists():
        copy_file(run_dir / "classification.json", classification_dest)

    return {
        "name": name,
        "workload": f"{prefix}/{wl_dest.name}",
        "baseline_metrics": (
            f"{prefix}/{metrics_dest.name}" if metrics_dest.exists() else None
        ),
        "server_features": (
            f"{prefix}/{features_dest.name}" if features_dest.exists() else None
        ),
        "classification": (
            f"{prefix}/{classification_dest.name}" if classification_dest.exists() else None
        ),
    }


def assemble_target_block(rec: dict, problem_dir: Path) -> dict:
    """Target lives directly in the problem dir, not in a subdir."""
    name = rec["workload_name"]
    run_dir = Path(rec["run_dir"]) if rec.get("run_dir") else None
    src_wl = Path(rec["workload_file"])

    # workload.yaml (named generically for the target)
    copy_file(src_wl, problem_dir / "workload.yaml")

    # baseline_metrics.json
    if run_dir:
        cand = list(run_dir.glob("*_metrics.json"))
        if cand:
            copy_file(cand[0], problem_dir / "baseline_metrics.json")
        if (run_dir / "server_features.json").exists():
            copy_file(run_dir / "server_features.json",
                      problem_dir / "server_features.json")
        if (run_dir / "classification.json").exists():
            copy_file(run_dir / "classification.json",
                      problem_dir / "classification.json")

    return {
        "workload": "workload.yaml",
        "baseline_metrics": "baseline_metrics.json",
        "server_features": "server_features.json",
        "classification": "classification.json",
        "profile_summary": None,
        "skip_L4_reason": "v0.4: setter agent did not opt in to deep profile",
        "repeats": [],
    }


